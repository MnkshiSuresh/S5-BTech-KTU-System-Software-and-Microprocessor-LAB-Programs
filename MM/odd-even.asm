DATA SEGMENT
    RESULT DW ?
    MSG DB 10,13,"ENTER THE NUMBER :$"
    MSG2 DB 10,13,"IS EVEN$"
    MSG3 DB 10,13,"IS ODD$"

DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    START:
    MOV AX,DATA
    MOV DS,AX

    LEA DX,MSG
    MOV AH,09H
    INT 21H

    MOV AH,01H
    INT 21H

    MOV BL,AL     
    MOV AX,BX
    MOV CX,0002H
    DIV CX
    MOV RESULT,DL  
    CMP DL, 0     
    JZ EVEN       

    LEA DX,MSG3   
    MOV AH,09H
    INT 21H
    JMP EXIT      

    EVEN:
    LEA DX,MSG2    
    MOV AH,09H
    INT 21H

    EXIT:
    MOV AH,4CH      
    INT 21H

CODE ENDS
END START
